package JavaBasics.Datatypes;

public class PrimitiveDataTypes {
  private static final boolean True = false;

  public static void main(String[] args) {
    
  byte b = 127; //can store values from -128 to 127
  short s = 1234; // 2 byte
  int i = 264732342; // 4 byte
  long l = 7327562376786L; // 8 byte
  float f = 271547.44345F;  //4 byte
  double d = 3274678.436576;  //8 byte
  char ch = 'A';  //2 byte
  boolean bb = true; // 1 byte
  System.out.println(bb);
}
}

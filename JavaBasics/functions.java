public class functions {

// Function to take input and print sum of two numbers.
    public static void printsum(int a, int b){
        int sum=a+b;
        System.out.println(sum);
    }


    //Function to take two int inputs and print multiplication
    public static void printmulti(int aa,int bb){
        int mul=aa*bb;
        System.out.println(mul);
    }
    public static void main(String[] args) {
        printsum(20, 11);
        printmulti(20,10);
    }
}
